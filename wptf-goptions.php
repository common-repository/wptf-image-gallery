<h3 id="usage"><strong>C) Usage Instructions</strong> - <a href="#toc">top</a></h3>
<p><b>Creating galleries : </b>From 'WPTF Gallery' tab, you can create, view manage and delete galleries. To create a new gallery, enter the details and press "create gallery". Gallery is now created with shortcode and settings you provided.</p>
<p><b>Adding images in gallery : </b>From 'WPTF Gallery' tab, click on 'Manage' in desired row. Here you add, edit and delete images for this particular gallery.</p>
<p><b>Editing settings : </b>From 'WPTF Gallery' tab, click on 'Settings' to open settings, here you can change the required options.</p>
<p><b>Displaying galleries : </b>From 'WPTF Gallery' tab, you can copy the shortcode for the desired gallery, then paste in any page, post, widget or custom post types to display the gallery. Additionally you can customize the gallery by customizing shortcode options.</p>
<p><b>Shortcode Options : </b>In the generated shortcode, if you can additionally define some options. They are explained below :</p>
<pre>
[wptf items_per_page="20" mode="mbox" navigation="bottom" id="4"]
</pre>
<ul>
<li><b>items_per_page : </b>Used to generate gallery pagination. Just input how many items should be shown on each page, rest happens automatically.</li>
<li><b>mode : </b>If you dont want description and comment systems, gallery can be launched directly in fullscreen mode by using <i><b>fullscreen</b></i> as value. otherwise dont use this property to load default mode.</li>
<li><b>navigation : </b>Used to define pagination location. You can use <i><b>top</b></i> to display pagination only at top, <i><b>bottom</b></i> to display pagination only at bottom, <i><b>both</b></i> to display pagination at top and bottom, both. <i><b>none</b></i> to hide pagination.</li>

</ul>

