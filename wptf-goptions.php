		<h3 id="Introduction"><strong>A) Introduction</strong> - <a href="#toc">top</a></h3>
		<p>WPTF is a WordPress plugin, using which an image gallery system can be built. Below are the main features of this Gallery plugin</p>
<ul>
<li>Simple, responsive and cross browser supported.</li>
<li>All modern mobile phones are supported, including default browsers of android and iphone.</li>
<li><b>Slideshow</b>, <b>Best Fit to screen</b>, <b>Crop Fit to screen</b> modes for images are available</li>
<li>Launch directly in fullscreen if don't want to view description and comments OR launch in description mode.</li>
<li>jQuery powered pagination is provided for smooth user experience and save load on server.</li>
<li>Multiple thumbnail styles to choose from.</li>
</ul>
