		<h3 id="points"><strong>D) Points to Remember</strong> - <a href="#toc">top</a></h3>
		
<ul>
<li>This script uses jQuery 1.10.2, without jQuery this plugin will not work.</li>
<li>You can upload a zip folder containing all images instead of uploading one by one.</li>
<li>Mobile mode is activated for all devices with screen resolution width lower than 800 pixels.<li>
<li>Slideshow is stopped after reaching last item.</li>
</ul>
		
